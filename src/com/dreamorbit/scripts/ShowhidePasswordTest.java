public class ShowhidePasswordTest extends BaseTest
{
	@Test(priority = 22, enabled = true)
	public void testShowHidePWD()  throws InterruptedException 
	{
		boolean showHidestatus;
		
		LoginPage loginPage = new LoginPage(driver);
		
		String un = read_XL_Data(XL_DATA_PATH, "ValidLoginData", 1, 0);
		String pwd = read_XL_Data(XL_DATA_PATH, "ValidLoginData", 1, 1);
		loginPage.setUN(un);
		loginPage.setPWD(pwd);
		
		 showHidestatus= loginPage.showHideState();
		 AssertJUnit.assertEquals(showHidestatus,true);
		 Reporter.log("Password is hidden on the first time input- testcase is passed",true);

		 for(int i=0;i<2;i++)
				{		 			 
			 loginPage.clickShowHide();
			 showHidestatus= loginPage.showHideState();
					if(showHidestatus==false)
					{
					 Reporter.log("Show Password testcase is passed",true);
					}
					else
					{
						 Reporter.log("Hide Password testcase is passed",true);
					}
		 }
		}
		
		
		
}