import com.dreamorbit.generic.BaseTest;
import com.dreamorbit.generic.Commons;
import com.dreamorbit.pages.StudiesPage;

public class VerifyTokenToggleBTN extends BaseTest {
	@Test(priority = 13, enabled = false)
	public void VerifyTokenCheck() throws InterruptedException {
		Commons commons = new Commons();

		StudiesPage studiesPage = new StudiesPage(driver);

		commons.login(driver);
		// Thread.sleep(3000);to be used
		driver.manage().timeouts().implicitlyWait(10, TimeUnit.SECONDS);
		commons.studiesScreenWait(driver);
		Thread.sleep(4000);
		for (int i = 0; i < 2; i++) {
			studiesPage.verifyTokenClick();
			commons.toastwait(driver);
			boolean verifyTokenStatus = studiesPage.verifyTokeBTNState();

			Thread.sleep(2000);
			String warningMSG = commons.getToastMSG(driver);
			//Reporter.log(warningMSG, true);
			AssertJUnit.assertTrue(warningMSG.equals("Verify token updated successfully"));
			Reporter.log("Verify Token updated successfully toast is shown-test case passed", true);
			if (verifyTokenStatus == true) {
				Reporter.log("Verify Token On test case is passed", true);
			} 
			else if (verifyTokenStatus == false)
			{
				Reporter.log("Verify Token OFF test case is passed", true);
			}
			else
			{
				Reporter.log("Verify Token ON/OFF test case is failed", true);
			}
			Thread.sleep(3000);
		}

	}
}
